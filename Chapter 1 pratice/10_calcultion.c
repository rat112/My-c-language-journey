#include <stdio.h>

int main()
{
    int a, b;
    printf("Enter your value a \n");
    scanf("%d", &a);

    printf("Enter your value b \n");
    scanf("%d", &b);

    printf("a and b sum value %d\n", a + b);
    printf("a and b sum value %d\n", a - b);
    printf("a and b sum value %d\n", a * b);
    return 0;
}